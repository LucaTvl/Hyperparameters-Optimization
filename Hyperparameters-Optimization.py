# ── GA Nivel 1: pesos continuos ───────────────────────────────
N_WEIGHTS = len(ZCP_COLS)
W_MIN, W_MAX = -1.0, 1.0

def random_weights():
    return [random.uniform(W_MIN, W_MAX) for _ in range(N_WEIGHTS)]

def weights_to_dict(w):
    return {col: w[i] for i, col in enumerate(ZCP_COLS)}

def fitness_weights(w, df_eval=None, k=50):
    if df_eval is None: df_eval = df_calib
    return precision_at_k(weights_to_dict(w), df_eval, k=k)

def blx_crossover(p1, p2, alpha=0.3):
    """BLX-α: hijo en intervalo [min-α*d, max+α*d]"""
    child = []
    for a, b in zip(p1, p2):
        lo, hi = min(a,b), max(a,b)
        d = hi - lo
        lo_ext = max(W_MIN, lo - alpha*d)
        hi_ext = min(W_MAX, hi + alpha*d)
        child.append(random.uniform(lo_ext, hi_ext))
    return child

def mutate_gaussian(ind, sigma=0.15):
    return [max(W_MIN, min(W_MAX, g + random.gauss(0, sigma)))
            for g in ind]

def tournament(pop, fits, k=3):
    idx = random.sample(range(len(pop)), k)
    return pop[max(idx, key=lambda i: fits[i])][:]

def run_ga_level1(pop_size=20, n_gen=5, p_cross=0.8,
                  p_mut=0.3, elite_k=2, sigma=0.15,
                  k_precision=50, seed=42):
    """
    GA para optimizar vector de pesos del proxy.
    Presupuesto total ≈ pop_size + (pop_size * n_gen) evaluaciones.
    Con pop_size=20, n_gen=5 → 100 evaluaciones (igual que BO).
    """
    random.seed(seed)
    pop  = [random_weights() for _ in range(pop_size)]
    fits = [fitness_weights(ind, k=k_precision) for ind in pop]
    history = [max(fits)]

    for gen in range(n_gen):
        sorted_idx = sorted(range(len(pop)), key=lambda i: fits[i], reverse=True)
        new_pop    = [pop[i][:] for i in sorted_idx[:elite_k]]

        while len(new_pop) < pop_size:
            p1 = tournament(pop, fits)
            p2 = tournament(pop, fits)
            child = blx_crossover(p1, p2) if random.random() < p_cross \
                    else p1[:]
            if random.random() < p_mut:
                child = mutate_gaussian(child, sigma)
            new_pop.append(child)

        pop  = new_pop
        fits = [fitness_weights(ind, k=k_precision) for ind in pop]
        history.append(max(fits))

    best_idx = max(range(len(pop)), key=lambda i: fits[i])
    return pop[best_idx], fits[best_idx], history

