def train_network(arch_str, C, num_cells, n_epochs=50, lr=0.025):
    """Entrena una TinyNetExt y retorna acc_valid por época."""
    net       = build_network_ext(arch_str, C=C, num_cells=num_cells)
    optimizer = optim.SGD(net.parameters(), lr=lr,
                          momentum=0.9, weight_decay=3e-4)
    scheduler = CosineAnnealingLR(optimizer, T_max=n_epochs)
    criterion = nn.CrossEntropyLoss()

    history = []
    for epoch in range(n_epochs):
        # ── Train ─────────────────────────────────────────────
        net.train()
        for xb, yb in train_loader:
            xb, yb = xb.to(DEVICE), yb.to(DEVICE)
            optimizer.zero_grad()
            out, _ = net(xb)
            criterion(out, yb).backward()
            optimizer.step()
        scheduler.step()

        # ── Validate ──────────────────────────────────────────
        net.eval()
        correct = total = 0
        with torch.no_grad():
            for xb, yb in val_loader:
                xb, yb = xb.to(DEVICE), yb.to(DEVICE)
                preds   = net(xb)[0].argmax(1)
                correct += (preds == yb).sum().item()
                total   += yb.size(0)
        acc = 100 * correct / total
        history.append(acc)

        if (epoch+1) % 10 == 0:
            print(f'    ep {epoch+1:3d}/{n_epochs}: acc={acc:.2f}%')

    return max(history), history, net

