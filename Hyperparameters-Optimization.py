"""## Bloque 5 — GA Nivel 1: optimización del vector de pesos del proxy

El GA busca el vector w ∈ [-1,1]⁶ que maximiza Precision@50 sobre la
calibración. Cada individuo es un vector de 6 reales (uno por ZCP).

**Operadores para espacio continuo:**
- Cruce: BLX-α (Blend Crossover) — genera hijos en el intervalo extendido entre padres
- Mutación: gaussiana con σ adaptativo
- Selección: torneo de tamaño 3
- Elitismo: top-3 pasan sin modificación

**Comparación justa con BO:**
- Mismo conjunto de calibración (1001 arqs)
- Mismo presupuesto: 100 evaluaciones de Precision@50
- 10 corridas independientes para medir estabilidad
"""

import random
