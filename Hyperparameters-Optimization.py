def build_network_ext(arch_str, n_classes=10, C=16, num_cells=5):
    """TinyNetwork con C y num_cells variables."""
    class TinyNetExt(nn.Module):
        def __init__(self):
            super().__init__()
            self.stem    = nn.Sequential(
                nn.Conv2d(3, C, 3, padding=1, bias=False),
                nn.BatchNorm2d(C))
            # stride=2 en posiciones 1 y (num_cells//2 + 1) si hay reducción
            strides = []
            for i in range(num_cells):
                if num_cells <= 3:
                    strides.append(2 if i == 1 else 1)
                else:
                    strides.append(2 if i in [1, num_cells//2+1] else 1)
            self.cells      = nn.ModuleList(
                [NATSCell(arch_str, C, s) for s in strides])
            self.lastact    = nn.Sequential(
                nn.BatchNorm2d(C), nn.ReLU(inplace=False))
            self.gap        = nn.AdaptiveAvgPool2d(1)
            self.classifier = nn.Linear(C, n_classes)
        def forward(self, x):
            x = self.stem(x)
            for cell in self.cells: x = cell(x)
            x = self.lastact(x)
            x = self.gap(x).view(x.size(0), -1)
            return self.classifier(x), None
    return TinyNetExt().to(DEVICE)

